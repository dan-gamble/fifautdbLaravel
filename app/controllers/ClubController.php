class ClubController extends \BaseController {

	public function index($id)
	{
        // Initialize the page
		$club = Club::where('asset_id', '=', $id)->firstOrFail();

        // Return the list of players
        $players = Player::orderBy('overall_rating', 'desc')->where('club_id', '=', $id)->get();

        // Create the view and parse variables
        return View::make('club.index', [
            'club' => $club,
            'players' => $players
        ]);
	}

